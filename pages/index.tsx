import type { NextPage, GetServerSideProps } from "next";
import prisma from "../lib/prisma";
import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";
import { Brand, Category } from "@prisma/client";

export type IServerItem = {
  id: number;
  price: number;
  working: boolean;
  Item: IServerItemDetails;
  Brand: Brand;
  Location: Location;
};

export type IServerItemDetails = {
  name: string;
  Category: Category;
};

const Home = ({ items }: { items: IServerItem[] }) => {
  console.log(items);

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h2>Inventory</h2>
      <ul>
        {items.map((item) => (
          <li className={styles.card} key={item.id}>
            <h3>
              {item.Item.name} {item.Item.Category.type}
            </h3>
            <div>{item.Brand.name}</div>
          </li>
        ))}
      </ul>
    </div>
  );
};

export const getServerSideProps: GetServerSideProps = async () => {
  const items = await prisma.itemInfo.findMany({
    select: {
      id: true,
      price: true,
      working: true,
      Brand: true,
      Comment: true,
      Location: true,
      Item: {
        select: {
          name: true,
          Category: true,
        },
      },
    },
  });
  return {
    props: {
      items,
    },
  };
};

export default Home;
