export const getServerSideProps: GetServerSideProps = async () => {
  const data = await prisma.category.findMany({
    select: {
      id: true,
      type: true,
      Item: {
        select: {
          name: true,
          _count: {}
        }
      }
    }
  });

  return {
    props: {
      data: data
    }
  };
};
